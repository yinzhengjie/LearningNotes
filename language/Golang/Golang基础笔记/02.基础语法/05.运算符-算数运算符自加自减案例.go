package main

import (
	"fmt"
)

func main() {

	a := 23
	b := 8

	fmt.Printf("a的类型为:[%T],a的值为[%d]\n", a, a)
	fmt.Printf("b的类型为:[%T],b的值为[%d]\n", b, b)

	/*
		自增和自减注意事项:
			自增表示对数值本身进行加一操作;
			自减表示对数值本身进行减一操作;
			自增自减符号只能写在变量后面,卸载变量前面会报语法错误;
			自增自减不能参与表达式计算;
	*/
	a++ //自增操作只能单独写成一行
	fmt.Printf("a的类型为:[%T],a的值为[%d]\n", a, a)

	b-- //自减操作只能单独写成一行
	fmt.Printf("b的类型为:[%T],b的值为[%d]\n", b, b)

}
